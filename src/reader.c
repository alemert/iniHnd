int iniReader( const char* fileName )
{
  int sysRc = 0 ;

  FILE* ini ;

  if( (ini = fopen( fileName, "r" ) ) == NULL )
  {
    logger( LSTD_OPEN_FILE_FAILED, fileName ) ;
    sysRc = 1 ;
    goto _door ;
  }

_door :

  return sysRc ;
}
